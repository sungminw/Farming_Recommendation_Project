package com.farm.myapp.sales.model;

public class FarmSalesCropVO {
	private int cropNum;
	private String cropName;
	private String cropEng;
	
	public int getCropNum() {
		return cropNum;
	}
	public void setCropNum(int cropNum) {
		this.cropNum = cropNum;
	}
	public String getCropName() {
		return cropName;
	}
	public void setCropName(String cropName) {
		this.cropName = cropName;
	}
	public String getCropEng() {
		return cropEng;
	}
	public void setCropEng(String cropEng) {
		this.cropEng = cropEng;
	}
	
	
}

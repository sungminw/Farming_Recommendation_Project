package com.farm.myapp.sales.model;

public class FarmSalesInfo {
	private String cropEng;
	private String areaEng;
	private int salesResult;
	
	public String getCropEng() {
		return cropEng;
	}
	public void setCropEng(String cropEng) {
		this.cropEng = cropEng;
	}
	public String getAreaEng() {
		return areaEng;
	}
	public void setAreaEng(String areaEng) {
		this.areaEng = areaEng;
	}
	public int getSalesResult() {
		return salesResult;
	}
	public void setSalesResult(int salesResult) {
		this.salesResult = salesResult;
	}
	
	
}
